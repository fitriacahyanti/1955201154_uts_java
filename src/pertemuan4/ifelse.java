public class ifelse {
    public static void main(String[] args) {
        int x=3;
        switch (x) {
            case 1:
                System.out.println("nilai x =1");
                break;
            case 2:
                System.out.println("nilai x = 2");
                break;
            case 3:
                System.out.println("nilai x = 3");
                break;
            default:
                System.out.println("nilai x tidak diketahui");
                break;
        }
           
    }
}
